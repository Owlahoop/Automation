Dic_1 = {"Key1": "Val1", 2: "Val2", "Key3": 32}

# Print dictionary
print(Dic_1)

# Print specific value, from key
print(Dic_1["Key3"])

# Update Key value
Dic_1["Key3"] = "Kevin"
print(Dic_1)

# Print all keys
print(Dic_1.keys())

# Print all values
print(Dic_1.values())

# Print all keys and values
print(Dic_1.items())

# Print length (amnt of items)
print(len(Dic_1))
