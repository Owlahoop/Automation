# Ways to define a string

# Use when an apostrophe is in the content
email = "g'mail@gmail.com"  # Double quotes

# Use when quotations are in the content
name = 'John "Jimmy" John'  # Single quotes

# Use for multiple lines
data = """
    This is line 1
    This is line 2
    This is line 3
"""  # Triple quotes

