data = "Hello World"

# Fetch specific character by using index
print(data[4])

# Fetch substring by start and end index
print(data[1:7])
# Prints character 1 to 6

# Fetch substring by giving start index only
print(data[1:])
# Prints character 1 to end

# Fetch substring by giving end index only
print(data[:7])
# Prints character 0 to 6
